selfish = '01234567'
        #  01234567

print(selfish[0])
print(selfish[7])

# [start:stop:step]
print(selfish[:5]) 
print(selfish[::1])
print(selfish[1:]) # start at 1, stop at end
print(selfish[0:8:2]) # step or stepover: str = 01234567 , if step = 2, str = 0246
print(selfish[::-1]) # reverse string
print(selfish[-1])

